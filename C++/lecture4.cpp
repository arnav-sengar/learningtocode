// VARIABLES :
// A variable is a container to hold data.
// Variable can be of various types.Primarily we have these variables types in C++ :
// int
// float
// char
// double
// bool

// int sum=34; means sum is an integer variable which holds value 34 in memory.

// syntax for declaring variables in c++ :
// <Data_type> <Variable_name> = Value;
// Ex : int a=4 , b=6;

// Rules to declare variables in c++ :
// Variable names in C++ can range from 1 to 255 characters.
// All variable names must begin with a letter of the alphabet or an underscore(_).
// After the first initial letter, variable names can also contain letters and numbers.
// Variables names are case-sensitive
// No spaces or special characters are allowed.
// You cannot use a c++ keyword(a reserved word) as a variable name.

// Variable Scope :
// Scope of a variable is the region in code where the existence of variable is valid.
// Based on scope we have local and global variables in C++.

// Local Variables :
// Local variable are declared inside the braces of any function and can be accessed only from there.

// Global Variables :
// Global variable are declared outside any function and can be accessed from anywhere.

// Can local and global variables have the same name in C++?
// YES!!!




